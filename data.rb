require 'io/console'


class Entity
    def self.print(msg)
        chr = @content[msg]
        if not chr.nil?
            str = Array.new
            str = chr.split("\n")
            for i in 0..str.length-1
                puts "\t\t" + str[i].to_s
            end
        end
    end
end




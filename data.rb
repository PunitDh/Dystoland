class Message < Entity
  def self.print(msg)
      maximum_length = winsize()[1].to_i - 36
      chr = @content[msg]
      
      str = Array.new
      if not chr.nil?
        if (chr.length > maximum_length)
          for i in 0..((chr.length/maximum_length))
            lastspace = chr[..(i*maximum_length)].rindex(" ")
            chr[lastspace] = "\n" if not lastspace.nil?
          end
